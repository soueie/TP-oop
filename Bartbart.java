    private void UpdatestudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdatestudentActionPerformed
            tblmodel = (DefaultTableModel) jTable1.getModel();//
        if(jTable1.getSelectedRowCount() == 1){
            String n = name.getText();
            String i = sex.getText();
            String p = course.getText();
            String y = student.getText();
            
            
            tblmodel.setValueAt(n, jTable1.getSelectedRow(), 0);
            tblmodel.setValueAt(i, jTable1.getSelectedRow(), 1);
            tblmodel.setValueAt(p, jTable1.getSelectedRow(), 2);
            tblmodel.setValueAt(y, jTable1.getSelectedRow(), 3);
            
            
            JOptionPane.showMessageDialog(this, "Update Sucessfully");
        }
        else{
            if(jTable1.getRowCount()==0){
                  JOptionPane.showMessageDialog(this, "The Table is Empty");
            }else{
                  JOptionPane.showMessageDialog(this, "Please Select Row to Update");
            }
        }
    }//GEN-LAST:event_UpdatestudentActionPerformed

