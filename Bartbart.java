    private void AddstudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddstudentActionPerformed
                                       
      if(name.getText().equals("")||sex.getText().equals("")||course.getText().equals("")
              ||student.getText().equals("")) {
          JOptionPane.showMessageDialog(this, "Please fill the blank");
   
    }                                      
      else{
          String hop[] = {name.getText(), sex.getText(), course.getText(),student.getText()};
          tblmodel = (DefaultTableModel)jTable1.getModel();
          tblmodel.addRow(hop);
           JOptionPane.showMessageDialog(null, "Listed");
          
          
                    
          name.setText("");
          sex.setText("");
          course.setText("");
          student.setText("");
          
      }
        // TODO add your handling code here:
    }//GEN-LAST:event_AddstudentActionPerformed

    private void RemovestudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemovestudentActionPerformed
    jTable1.setRowSelectionAllowed(true);    
        int row = jTable1.getSelectedRow();
         if(row == -1) {
                    JOptionPane.showMessageDialog(null, "Please select a row to delete");
                }else {
                    int confirm = JOptionPane.showConfirmDialog(null, "Permanently delete?", "Delete", JOptionPane.YES_NO_OPTION);
                    if(confirm == JOptionPane.YES_OPTION) {
                     tblmodel.removeRow(row);
    }                                   
         }
    // TODO add your handling code here:
    }//GEN-LAST:event_RemovestudentActionPerformed

